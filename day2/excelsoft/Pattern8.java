package excelsoft;

public class Pattern8 {
	public static void main(String[] args) {
int n = 5;
		
		int i, j;
		 
        for (i = n-1; i >= 0; i--) {
 
            
            for (j = 0; j <= i; j++) {
                System.out.print(i+1 + " ");
            }
 
       
            System.out.println();
		
	          
	        
        }
	}

}
